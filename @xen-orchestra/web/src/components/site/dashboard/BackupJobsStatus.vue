<template>
  <UiCard :has-error>
    <UiCardTitle>
      {{ t('backups.jobs-status') }}
      <template #info>
        <UiLink size="small" to="/backups"> {{ t('see-all') }}</UiLink>
      </template>
    </UiCardTitle>
    <VtsStateHero v-if="!areBackupsJobsReady" format="card" busy size="medium" />
    <VtsStateHero v-else-if="hasError" format="card" type="error" size="medium">
      {{ t('error-no-data') }}
    </VtsStateHero>
    <VtsStateHero v-else-if="!backups" format="card" type="no-data" horizontal size="medium">
      {{ t('no-data-to-calculate') }}
    </VtsStateHero>
    <template v-else>
      <VtsDonutChartWithLegend :segments="jobsSegments" :title="jobsTitle" />
      <UiCardNumbers :label="t('total')" :value="backups.jobs.total" size="small" />
    </template>
  </UiCard>
</template>

<script lang="ts" setup>
import type { XoDashboard } from '@/types/xo/dashboard.type.ts'
import VtsDonutChartWithLegend, {
  type DonutChartWithLegendProps,
} from '@core/components/donut-chart-with-legend/VtsDonutChartWithLegend.vue'
import VtsStateHero from '@core/components/state-hero/VtsStateHero.vue'
import UiCard from '@core/components/ui/card/UiCard.vue'
import UiCardNumbers from '@core/components/ui/card-numbers/UiCardNumbers.vue'
import UiCardTitle from '@core/components/ui/card-title/UiCardTitle.vue'
import UiLink from '@core/components/ui/link/UiLink.vue'
import { computed } from 'vue'
import { useI18n } from 'vue-i18n'

const { backups } = defineProps<{
  backups: XoDashboard['backups'] | undefined
  hasError?: boolean
}>()

const areBackupsJobsReady = computed(() => backups?.jobs !== undefined)

const { t } = useI18n()

const jobsTitle = computed<DonutChartWithLegendProps['title']>(() => ({
  label: t('backups.jobs.last-seven-days'),
}))

const jobsSegments = computed<DonutChartWithLegendProps['segments']>(() => [
  {
    label: t('backups.jobs.running-good'),
    value: backups?.jobs.successful ?? 0,
    accent: 'success',
  },
  {
    label: t('backups.jobs.skipped-runs'),
    value: backups?.jobs.skipped ?? 0,
    accent: 'warning',
  },
  {
    label: t('backups.jobs.errors-detected'),
    value: backups?.jobs.failed ?? 0,
    accent: 'danger',
  },
  {
    label: t('backups.jobs.disabled'),
    value: backups?.jobs.disabled ?? 0,
    accent: 'muted',
  },
])
</script>
